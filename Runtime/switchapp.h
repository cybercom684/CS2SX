#define CS2SX_RGBA(r,g,b,a) (((u32)(a) << 24) | ((u32)(b) << 16) | ((u32)(g) << 8) | (u32)(r))
#define CS2SX_RGB(r,g,b)    CS2SX_RGBA(r,g,b,255)

#define COLOR_BLACK   CS2SX_RGB(0,   0,   0  )
